/* Size of a font mania (.com) executable header.  The file is charset data up to the footer. */
#define FONT_MANIA_HEADER_SIZE 1442
#define FONT_MANIA_FOOTER_SIZE 1
#define FONT_MANIA_FILE_SIZE (FONT_MANIA_HEADER_SIZE + CHARSET_SIZE + FONT_MANIA_FOOTER_SIZE)



charset *charset_load(char *path) {
        FILE *fp = fopen(path, "rb");
        if(!fp) {
                return NULL;
        }

        // Detect the file type by its size
        fseek(fp, 0, SEEK_END);
        size_t file_size = ftell(fp);
        if(file_size == CHARSET_SIZE) {
                // Raw charset data
                fseek(fp, 0, SEEK_SET);
        } else if(file_size == FONT_MANIA_FILE_SIZE) {
                // Font mania .com files have charset data after the header
                fseek(fp, FONT_MANIA_HEADER_SIZE, SEEK_SET);
        } else {
                // Unknown size, bail out
                fclose(fp);
                return NULL;
        }

        // We previously fseek'd to the start of the charset data
        charset *result = malloc(sizeof(charset));
        result->path = strdup(path);
        if(fread(result->data, 1, CHARSET_SIZE, fp) != CHARSET_SIZE) {
                // Read error
                free(result);
                result = NULL;
        }
        fclose(fp);
        return result;
}

void charset_free(charset *char_set) {
        free(char_set->path);
        free(char_set);
}

